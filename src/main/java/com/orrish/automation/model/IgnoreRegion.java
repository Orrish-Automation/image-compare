package com.orrish.automation.model;

public class IgnoreRegion {

    public int startX, startY, endX, endY;

    public IgnoreRegion(int startX, int startY, int endX, int endY) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
}
